/**
 * @file packages/feature-bots/src/dto/TestBotRequest.ts
 * ============================================================================
 * 🤖 FEATURE-BOTS — TestBotRequest DTO
 * ============================================================================
 *
 * Архитектурная роль:
 * - DTO-агрегат TestBotRequest: контракт тестового запроса к боту для API boundary.
 * - Используется bot endpoints для тестирования бота (отправка сообщения и получение ответа).
 * - Поддерживает опциональные conversationId и context для гибкого тестирования.
 * - Безопасен для API boundary (не раскрывает внутренние детали).
 *
 * Принципы:
 * - ✅ SRP: только структура запроса тестирования (без бизнес-логики и transport-деталей).
 * - ✅ DTO-pure: без HTTP/DB-деталей, только типы контрактов и инварианты.
 * - ✅ Immutable: все поля readonly для integrity и type-safety.
 * - ✅ API-friendly: структурированный запрос, удобный для UI и SDK.
 */

import type { ID, JsonObject } from '@livai/core-contracts';

/* ============================================================================
 * 🧩 TEST BOT REQUEST TYPES
 * ========================================================================== */

/**
 * DTO запроса тестирования бота.
 *
 * Инварианты:
 * - message: непустая строка (после trim).
 * - conversationId: если задан, должен быть валидным UUID.
 * - context: если задан, должен быть валидным JSON объектом.
 *
 * @remarks
 * Runtime validation выполняется в lib/bot-request-validator.ts.
 *
 * Воспроизводимость тестов:
 * - Если conversationId не задан, создаётся новый разговор для теста (зависит от external state).
 * - Для воспроизводимых тестов рекомендуется явно указывать conversationId.
 * - Backend должен логировать создание нового разговора для отслеживания тестовых сессий.
 *
 * Extensibility:
 * - Patch pattern позволяет добавлять новые поля без изменения структуры DTO.
 * - При добавлении новых обязательных полей (кроме message) можно использовать AtLeastOne паттерн.
 */
export type TestBotRequest = Readonly<{
  /**
   * Сообщение для тестирования бота (обязательно).
   * Отправляется боту для получения ответа.
   */
  readonly message: string;
  /**
   * Идентификатор разговора (опционально).
   * Если задан, сообщение добавляется в существующий разговор.
   * Если не задан, создаётся новый разговор для теста (поведение зависит от external state).
   *
   * @remarks
   * Для воспроизводимых тестов рекомендуется явно указывать conversationId.
   * Backend должен логировать создание нового разговора при отсутствии conversationId.
   */
  readonly conversationId?: ID<'Conversation'>;
  /**
   * Контекст для тестирования (опционально).
   * Используется для передачи дополнительных данных в бот (метаданные, переменные окружения и т.д.).
   */
  readonly context?: JsonObject;
}>;
