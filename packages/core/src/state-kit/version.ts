/* ============================================================================
 * 🔢 COMPARE — СРАВНЕНИЕ ВЕРСИЙ
 * ========================================================================== */

/**
 * Сравнивает две числовые версии.
 *
 * @param {number} a - ожидаемая/текущая версия
 * @param {number} b - загруженная/сохранённая версия
 * @returns {-1 | 0 | 1} -1 если a < b, 0 если равны, 1 если a > b
 */
export function compareVersion(a: number, b: number): -1 | 0 | 1 {
  if (a === b) return 0;
  return a < b ? -1 : 1;
}

/* ============================================================================
 * ✅ ASSERT — ПРОВЕРКА РАВЕНСТВА ВЕРСИЙ
 * ========================================================================== */

/**
 * Бросает ошибку в dev, если версии не совпадают.
 * В production режиме может быть no-op или деградировать до логирования
 * (на усмотрение вызывающего кода).
 *
 * @param {number} a - ожидаемая/текущая версия
 * @param {number} b - загруженная/сохранённая версия
 */
export function assertVersionEqual(a: number, b: number): void {
  if (a === b) return;

  // В dev режиме бросаем ошибку; в prod вызывающий код может выбирать поведение.
  /* eslint-disable fp/no-throw -- намеренный dev-only assert в core helpers */
  if (process.env['NODE_ENV'] !== 'production') {
    throw new Error(`State version mismatch: expected ${a}, got ${b}`);
  }
  /* eslint-enable fp/no-throw */
}

/* ============================================================================
 * ❓ IS VERSION MISMATCH — УПРОЩЁННАЯ ПРОВЕРКА
 * ========================================================================== */

/**
 * Возвращает true, если версии несовместимы (a !== b).
 * Удобен для простых ветвлений без выброса исключений.
 *
 * @param {number} a - ожидаемая/текущая версия
 * @param {number} b - загруженная/сохранённая версия
 */
export function isVersionMismatch(a: number, b: number): boolean {
  return a !== b;
}
